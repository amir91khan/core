package typeconversion;

public class Demo15 {

	public static void main(String[] args) {
		int n=3;
		switch(n)
		{
		case 1:
			n=n+1;
			break;
		case 3:
			n=n+5;
			break;
		case 4:
		    n=n+4;
		    break;
		    default:
		    	n=n-2;
		    	
		    
		}
		System.out.println(n);

	}

}
